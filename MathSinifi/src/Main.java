public class Main
{
    public static void main(String[] args)
    {
        // Math sınıfı
        System.out.println(Math.ceil(8.6)); // girdiğimiz değeri bir büyük tam sayıya yuvarlar
        System.out.println(Math.cos(45));  // cosinus değerini hesaplar
        System.out.println(Math.sin(45)); // sinus degerini hesaplar
        System.out.println(Math.floor(2.4)); // bir alt değere yuvarlar..
        System.out.println(Math.max(2,4)); // 2 değer arasında büyük olanı gösterir
        System.out.println(Math.min(2,4)); // 2 değer arasında küçük olanı gösterir
        System.out.println(Math.pow(2,5)); // üs'sünü hesaplar
        System.out.println(Math.random()); // 0-1 arasında random bir sayı üretir
        System.out.println(Math.round(12.7)); // yakın olan tam sayıya yuvarlar...
    }
}
